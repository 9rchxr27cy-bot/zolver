const translations = {
  EN: enTranslations,
  PT: ptTranslations,
  FR: frTranslations,
  DE: enTranslations, // Fallback to English for brevity, ideally would be German
  LB: frTranslations  // Fallback to French for Luxembourgish (common convention if LB missing)
};

interface LanguageContextProps {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: typeof enTranslations;
  tCategory: (catId: string) => string;
}

const LanguageContext = createContext<LanguageContextProps | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>('FR');

  const t = translations[language] || translations['EN'];

  // FIX: Properly look up category translation
  const tCategory = (catId: string) => {
    const langMap = CATEGORY_TRANSLATIONS[language];
    if (langMap && langMap[catId]) {
        return langMap[catId];
    }
    // Fallback to English/ID if not found
    return catId; 
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, tCategory }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
